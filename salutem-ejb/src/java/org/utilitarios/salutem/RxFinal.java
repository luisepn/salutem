package org.utilitarios.salutem;

/**
 *
 *
 */
public class RxFinal {

    private String ojo;
    private String esfera;
    private String cilindro;
    private String eje;
    private String adicion;
    private String distanciapupilar;
    private String agudezavisual;

    public RxFinal(String ojo,String esfera, String cilindro, String eje, String adicion, String distanciapupilar, String agudezavisual) {
        this.ojo = ojo;
        this.esfera = esfera;
        this.cilindro = cilindro;
        this.eje = eje;
        this.adicion = adicion;
        this.distanciapupilar = distanciapupilar;
        this.agudezavisual = agudezavisual;
    }

    /**
     * @return the ojo
     */
    public String getOjo() {
        return ojo;
    }

    /**
     * @param ojo the ojo to set
     */
    public void setOjo(String ojo) {
        this.ojo = ojo;
    }

    /**
     * @return the esfera
     */
    public String getEsfera() {
        return esfera;
    }

    /**
     * @param esfera the esfera to set
     */
    public void setEsfera(String esfera) {
        this.esfera = esfera;
    }

    /**
     * @return the cilindro
     */
    public String getCilindro() {
        return cilindro;
    }

    /**
     * @param cilindro the cilindro to set
     */
    public void setCilindro(String cilindro) {
        this.cilindro = cilindro;
    }

    /**
     * @return the eje
     */
    public String getEje() {
        return eje;
    }

    /**
     * @param eje the eje to set
     */
    public void setEje(String eje) {
        this.eje = eje;
    }

    /**
     * @return the adicion
     */
    public String getAdicion() {
        return adicion;
    }

    /**
     * @param adicion the adicion to set
     */
    public void setAdicion(String adicion) {
        this.adicion = adicion;
    }

    /**
     * @return the distanciapupilar
     */
    public String getDistanciapupilar() {
        return distanciapupilar;
    }

    /**
     * @param distanciapupilar the distanciapupilar to set
     */
    public void setDistanciapupilar(String distanciapupilar) {
        this.distanciapupilar = distanciapupilar;
    }

    /**
     * @return the agudezavisual
     */
    public String getAgudezavisual() {
        return agudezavisual;
    }

    /**
     * @param agudezavisual the agudezavisual to set
     */
    public void setAgudezavisual(String agudezavisual) {
        this.agudezavisual = agudezavisual;
    }

}
